def make_recommendation(top_diagnosis_code: str, symptoms: dict) -> str:
    itching = int(symptoms.get("itching", 0))
    pain = int(symptoms.get("pain", 0))
    bleeding = int(symptoms.get("bleeding", 0))
    peeling = int(symptoms.get("peeling", 0))
    affected_area = float(symptoms.get("affected_area", 0))
    evolution = symptoms.get("evolution", [])

    if not isinstance(evolution, list):
        evolution = [evolution] if evolution else []

    high_risk = bleeding == 1 or any(
        ev in {"growing", "color_change", "shape_change"} for ev in evolution
    )

    code = (top_diagnosis_code or "").lower()

    # Онкология
    if high_risk or "melanoma" in code or "carcinoma" in code or "basal" in code:
        return (
            "Присутствуют признаки, требующие очной оценки (возможная опухоль или изменения). "
            "Обратитесь к дерматологу немедленно."
        )

    # Псориаз
    if "psoriasis" in code:
        return (
            "Вероятно псориаз: увлажнение кожи, мягкие кератолитики, при выраженных высыпаниях "
            "консультация дерматолога для подбора топических/системных средств."
        )

    # Атопический/контактный дерматит и экзема
    if "dermatitis" in code or "eczema" in code or "atopic" in code:
        return (
            "Скорее всего воспалительное кожное заболевание (дерматит/экзема). "
            "Рекомендовано регулярное увлажнение, избегание раздражителей; при выраженном зуде/воспалении — консультация врача."
        )

    # Себорейный дерматит
    if "seborrheic" in code:
        return (
            "Себорейный дерматит: противогрибковые шампуни для кожи головы, мягкие эмоленты для лица; "
            "при неэффективности — консультация дерматолога."
        )

    # Акне и родственные состояния
    if "acne" in code or "pomade" in code or "steroid_acne" in code:
        return (
            "Вероятно акне: соблюдение гигиены, некомедогенные средства ухода, при воспалении — консультация дерматолога для назначения топических/системных препаратов."
        )

    # Грибковые инфекции
    if "tinea" in code or "candid" in code or "dermatophyt" in code:
        return (
            "Возможна грибковая инфекция. Местные противогрибковые средства часто эффективны; "
            "при поражении ногтей или обширном процессе — обратиться к врачу."
        )

    # Ногти
    if "onych" in code or "nail" in code:
        return (
            "Поражение ногтей: лечение длительное (антимикотики/локальные средства). "
            "Рекомендуется очная оценка и обследование у дерматолога/подолога."
        )

    # Герпес
    if "herpes" in code:
        return (
            "Возможно вирусное поражение (герпес): при острой боли/образованиях в первые дни возможно назначение противовирусных средств — обратитесь к врачу."
        )

    # Пигментация / мелазма / витилиго
    if "melasma" in code or "pigment" in code:
        return (
            "Пигментация: солнцезащита, отбеливающие/topical средства по рекомендации дерматолога. "
            "Консультация для подбора терапии."
        )

    if "vitiligo" in code:
        return (
            "Витилиго: консультация дерматолога/фототерапия и топические препараты могут быть рассмотрены. "
            "Рекомендуется очная оценка."
        )

    # Доброкачественные выпуклости (кератозы, папилломы и т.п.)
    if "keratosis" in code or "seborrheic_keratosis" in code or "benign" in code:
        return (
            "Скорее всего доброкачественное образование. Наблюдение допустимо, однако при изменениях/кровотечении — очная консультация."
        )

    # Невусы
    if "nevus" in code:
        return (
            "Невус: обычно доброкачественно, наблюдение за изменениями (размер, цвет, форма). "
            "При изменении — очная оценка у дерматолога/дерматоонколога."
        )

    # Розацеа
    if "rosacea" in code:
        return (
            "Розацеа: избегать триггеров (алкоголь, горячие напитки), мягкие местные средства; при выраженных симптомах — консультация дерматолога."
        )

    # Общие рекомендации при выраженных симптомах
    if affected_area >= 20 or (itching == 1 and pain == 1) or peeling == 1:
        return (
            "Рекомендуется консультация дерматолога. При усилении симптомов — немедленно обратиться к врачу."
        )

    # Низкий/умеренный риск — наблюдение и базовый уход
    return (
        "По представленным данным возможна доброкачественная или воспалительная патология. "
        "Рекомендуется базовый уход (увлажнение, избегание раздражителей, солнцезащита) и при необходимости плановая консультация дерматолога."
    )